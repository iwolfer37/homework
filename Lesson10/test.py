import xlwt

# Створення нового xls-документу
workbook = xlwt.Workbook()

# Створення листа у документі
sheet = workbook.add_sheet('Автомобілі')

# Заголовки стовпців
headers = ['Марка', 'Вартість', 'Споживання палива', 'Максимальна швидкість', 'Вантажопідйомність']

# Запис заголовків у перший рядок
for col, header in enumerate(headers):
    sheet.write(0, col, header)

# Дані про автомобілі
data = [
    ['Audi', 50000, 10.5, 250, 500],
    ['BMW', 60000, 11.2, 240, 550],
    ['Mercedes', 70000, 12.8, 230, 600],
    ['Toyota', 40000, 9.5, 220, 450],
    ['Honda', 35000, 9.0, 210, 400],
    ['Ford', 45000, 10.0, 240, 550],
    ['Chevrolet', 38000, 9.8, 230, 500],
    ['Volkswagen', 42000, 9.2, 220, 480],
    ['Nissan', 39000, 9.6, 210, 450],
    ['Hyundai', 32000, 9.4, 200, 400],
    ['Kia', 33000, 9.5, 205, 420],
    ['Mazda', 36000, 10.2, 215, 460],
    ['Subaru', 55000, 11.5, 240, 550],
    ['Lexus', 68000, 12.5, 250, 600],
    ['Volvo', 75000, 13.2, 240, 580],
    ['Peugeot', 52000, 10.8, 230, 520],
    ['Renault', 45000, 10.5, 220, 500],
    ['Fiat', 32000, 9.7, 210, 400],
    ['Jeep', 60000, 11.8, 235, 550],
    ['Land Rover', 80000, 14.0, 260, 650],
]

# Запис даних у наступні рядки
for row, car in enumerate(data, start=1):
    for col, value in enumerate(car):
        sheet.write(row, col, value)

# Збереження xls-документу
workbook.save('автомобілі.xls')

